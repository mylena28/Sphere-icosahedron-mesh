      subroutine create_icosahedron(v,f,nv,nf)
      implicit none

      double precision v(3,*)
      integer f(3,*)
      integer nv,nf

      double precision phi
      phi = (1.0d0 + sqrt(5.0d0))/2.0d0

      nv = 12
      nf = 20

! vertices
      v(1,1)=-1.d0; v(2,1)= phi; v(3,1)=0.d0
      v(1,2)= 1.d0; v(2,2)= phi; v(3,2)=0.d0
      v(1,3)=-1.d0; v(2,3)=-phi; v(3,3)=0.d0
      v(1,4)= 1.d0; v(2,4)=-phi; v(3,4)=0.d0

      v(1,5)=0.d0; v(2,5)=-1.d0; v(3,5)= phi
      v(1,6)=0.d0; v(2,6)= 1.d0; v(3,6)= phi
      v(1,7)=0.d0; v(2,7)=-1.d0; v(3,7)=-phi
      v(1,8)=0.d0; v(2,8)= 1.d0; v(3,8)=-phi

      v(1,9)= phi; v(2,9)=0.d0; v(3,9)=-1.d0
      v(1,10)= phi; v(2,10)=0.d0; v(3,10)= 1.d0
      v(1,11)=-phi; v(2,11)=0.d0; v(3,11)=-1.d0
      v(1,12)=-phi; v(2,12)=0.d0; v(3,12)= 1.d0

! faces
      f(:,1)=(/1,12,6/)
      f(:,2)=(/1,6,2/)
      f(:,3)=(/1,2,8/)
      f(:,4)=(/1,8,11/)
      f(:,5)=(/1,11,12/)

      f(:,6)=(/2,6,10/)
      f(:,7)=(/6,12,5/)
      f(:,8)=(/12,11,3/)
      f(:,9)=(/11,8,7/)
      f(:,10)=(/8,2,9/)

      f(:,11)=(/4,10,5/)
      f(:,12)=(/4,5,3/)
      f(:,13)=(/4,3,7/)
      f(:,14)=(/4,7,9/)
      f(:,15)=(/4,9,10/)

      f(:,16)=(/5,10,6/)
      f(:,17)=(/3,5,12/)
      f(:,18)=(/7,3,11/)
      f(:,19)=(/9,7,8/)
      f(:,20)=(/10,9,2/)

      return
      end
